#include <unistd.h>

int	ft_putstr_fd(char *s, int fd)
{
	int	i;
	int	length;

	i = 0;
	length = 0;
	while (s[i] != '\0')
	{
		write(fd, &s[i], 1);
		length++;
		++i;
	}
	return (length);
}
